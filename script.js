function red(der, far) {

    if (der > 2 && far > 2) {
        return der * 2 && far * 2
    } else if (der < 3 && far < 3) {
        return der + 3 && far + 3
    } else if (der > 2 && far < 3) {
        return der, far
    }

}

let der, far = red(2, 7);
console.log(der, far)



